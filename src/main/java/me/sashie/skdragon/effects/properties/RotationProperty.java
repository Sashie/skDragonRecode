package me.sashie.skdragon.effects.properties;

public class RotationProperty {

	boolean rotating;
	boolean rotatingWithPlayer;

	public boolean isRotating() {
		return rotating;
	}

	public void setRotating(boolean rotating) {
		this.rotating = rotating;
	}

	public boolean isRotatingWithPlayer() {
		return rotatingWithPlayer;
	}

	public  void setRotatingWithPlayer(boolean rotatingWithPlayer) {
		this.rotatingWithPlayer = rotatingWithPlayer;
	}
}
